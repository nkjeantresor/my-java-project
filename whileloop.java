// while loop progam
public class whileloop {
    public static void main(String[] args) {
        int i=0;
        System.out.println("displaying the first 10 even numbers");
        while (i<=10)// the condition that should be satisfied  and the execution will be proceed until the condition is met
         {
            System.out.println(i);
            i=i+2;
            
            
        }
    }
}
