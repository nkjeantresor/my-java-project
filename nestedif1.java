// this nested if shows the condition where there is more than two options as this example shows 
public class nestedif1 {
    public static void main(String[] args) {
        
        int no=1;// initialises number to one  and display the output corresponding to the condition satisfied
        if(no>2){System.out.println("hello1");}
        else if(no<2){System.out.println("hello2");}
        else if(no==5){System.out.println("hello3");}
        else{System.out.println("hello4");}
    }
    
}
